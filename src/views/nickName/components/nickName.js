export default class NickNameObject {
  constructor(id, name, nickName, type, creator, remark, status, pageNum, pageSize) {
    this.id = id
    this.name = name
    this.nickName = nickName
    this.type = type
    this.creator = creator
    this.remark = remark
    this.status = status
    this.pageNum = pageNum
    this.pageSize = pageSize
  }
}
